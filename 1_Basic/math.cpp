#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    int n1 = 5, n2 = 7;
    cout << max(n1, n2) << endl;
    cout << min(n1, n2) << endl;

    cout << sqrt(81) << endl;
    cout << pow(7, 3) << endl;
    cout << round(81.6) << endl;
    cout << ceil(73.1) << endl;
    cout << floor(81.8) << endl;
    cout << log(2) << endl;
}